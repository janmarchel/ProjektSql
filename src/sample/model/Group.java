package sample.model;

public class Group {
    private int group_id;
    private int group_nr;
    private int student_nr;

    public int getGroup_id() {
        return group_id;
    }

    public void setGroup_id(int group_id) {
        this.group_id = group_id;
    }

    public int getGroup_nr() {
        return group_nr;
    }

    public void setGroup_nr(int group_nr) {
        this.group_nr = group_nr;
    }

    public int getStudent_nr() {
        return student_nr;
    }

    public void setStudent_nr(int student_nr) {
        this.student_nr = student_nr;
    }
}
